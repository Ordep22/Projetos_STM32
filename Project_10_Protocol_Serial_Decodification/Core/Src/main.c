void checkSum(uint8_t *Byte, int tamanho)
{
	//Adicionar uma vriável chamda tamanho para controlar até onde o for irá

	unsigned char dados[1]= {0};

	unsigned char *pointer;


	//int posicao = 0;

	pointer = &dados[1];


	for (int i = 0; i < tamanho; i++)
	{

		if (Byte[i] != 0)


		{

			HAL_Delay(100);
			dados[1] = dados[1] + Byte[i];

		}

		else
		{
			break;

		}

	}

	HAL_UART_Transmit(&huart2,pointer,1,100);



	for (int y = 0; y < tamanho; y++ )
	{

		Byte[y] = 0;

		if (y < 2)
		{

			dados[y] = 0;

		}

	}


	tamanho  = 0;

}





